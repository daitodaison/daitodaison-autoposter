def select_template(kw):
    """キーワードから最適な型を自動選択"""
    scores = {}
    for key, tpl in ARTICLE_TEMPLATES.items():
        if key == "DEFAULT":
            continue
        scores[key] = sum(1 for trigger in tpl["triggers"] if trigger in kw)

    best_key = max(scores, key=lambda k: scores[k]) if scores else "DEFAULT"
    if not scores or scores.get(best_key, 0) == 0:
        log.info("  >> 記事テンプレート: DEFAULT（汎用型）")
        return ARTICLE_TEMPLATES["DEFAULT"]

    log.info(f"  >> 記事テンプレート: {ARTICLE_TEMPLATES[best_key]['name']}（スコア:{scores[best_key]}）")
    return ARTICLE_TEMPLATES[best_key]


def determine_goal(kw):
    """キーワードからCONVERSION_GOALSを判定"""
    matches = []
    for goal_key, goal in CONVERSION_GOALS.items():
        if goal_key == "default":
            continue
        score = 0
        for trigger in goal["triggers"]:
            if " " in trigger:
                parts = trigger.split(" ")
                if all(p in kw for p in parts):
                    score += 2
            else:
                if trigger in kw:
                    score += 1
        if score > 0:
            matches.append({"key": goal_key, "goal": goal, "score": score})

    if not matches:
        log.info("  >> ゴール判定: default")
        return {"key": "default", "goal": CONVERSION_GOALS["default"]}

    matches.sort(key=lambda m: (-m["score"], m["goal"]["priority"]))
    best = matches[0]
    log.info(f"  >> ゴール: [{best['key']}] score={best['score']} → {best['goal']['url']}")
    return {"key": best["key"], "goal": best["goal"]}


def get_cta(kw, position):
    """誘導文取得"""
    result = determine_goal(kw)
    goal = result["goal"]
    return goal["cta"].get(position, goal["cta"].get("末尾", ""))
